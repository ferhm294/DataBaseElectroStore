/*Sentencia 2: consulta de facturas con sus respectivos detalles*/

SELECT FE.NumFactura AS 'Número de Factura',
FE.FecCompra AS 'Fecha de la Compra',
CC.Nombre + ' ' + CC.Apellido1 + ' ' + CC.Apellido2 AS 'Cliente',
EE.Nombre + ' ' + EE.Apellido1 + ' ' + EE.Apellido2 AS 'Empleado',
FE.Total,
FE.Estado,
PP.Descripcion AS 'Producto',
FD.Cantidad,
FD.PrecioUnitario
FROM ELEFacturaEnc FE
INNER JOIN ELEClientes CC ON FE.CodCliente = CC.CodCliente
INNER JOIN ELEEmpleados EE ON FE.CodEmpleado = EE.CodEmpleado
INNER JOIN ELEFacturasDet FD ON FE.NumFactura = FD.NumFactura
INNER JOIN ELEProductos PP ON FD.CodProducto = PP.CodProducto;

/*Sentencia 3: consulta de ventas por tienda*/

SELECT TT.Nombre AS 'Nombre Tienda',
SUM(FE.Total) AS 'Total Ventas',
COUNT(FE.NumFactura) AS 'Número de Facturas'
FROM ELEFacturaEnc FE
INNER JOIN ELETiendas TT ON FE.CodTienda = TT.CodTienda
GROUP BY TT.Nombre;

/*Sentencia 4: Total de compras por clientes para clientes que han hecho más de 2 compras*/

SELECT CC.Identificacion AS 'Nímero de Identificación',
CC.Nombre + ' ' + CC.Apellido1 + ' ' + CC.Apellido2 AS 'Nombre Cliente',
COUNT(FE.NumFactura) AS 'Cantidad de Compras',
SUM(FE.Total) AS 'Total Pagado'
FROM ELEFacturaEnc FE
INNER JOIN ELEClientes CC ON FE.CodCliente = CC.CodCliente
GROUP BY CC.Identificacion,
CC.Nombre,
CC.Apellido1,
CC.Apellido2
HAVING COUNT(FE.NumFactura) > 2;

/*Sentencia 5: atrasar 3 días la fecha de entrega de las compras cuya fecha de entrega esté a menos de 4 días de la fecha de la compra*/

/*En el insert hubo un error ya que al poner ' ' en la fecha de entrega el sistema lo puso con fecha por defecto de 1900-01-01, dado este error
el motor de base de datos no es capaz de actualizar las fechas de entrega correctamente ya que la diferencia siempre va a ser más de 4 días (años).
Para solucionarlo, agregué un update adicional para reemplazar la fecha por defecto con NULL y en el script del update solicitado para esta
sentencia evaluamos que si la fecha de entrega es NULL se use la fecha de compra como base para hacer el update final de la fecha de entrega.*/

UPDATE ELEFacturaEnc
SET FecEntrega = NULL
WHERE FecEntrega = '1900-01-01';

/*Una vez curados los datos, nuestro update ya va a funcionar como lo solicita el enunciado.*/

UPDATE FE
SET FE.FecEntrega = DATEADD(DAY, 3, ISNULL(FE.FecEntrega, FE.FecCompra))
FROM ELEFacturaEnc FE
WHERE DATEDIFF(DAY, FE.FecCompra, ISNULL(FE.FecEntrega, FE.FecCompra)) < 4;